function getIconHelper() {
  const cache = new Map<string, ReactNode>();

  cache.set('js', icons.javascript);
  cache.set('jsx', icons.reactJS);
  cache.set('ts', icons.typescript);
  cache.set('tsx', icons.reactTS);

  cache.set('css', icons.css);
  cache.set('json', icons.json);
  cache.set('html', icons.html);
  cache.set('png', icons.image);
  cache.set('jpg', icons.image);

  cache.set('ico', icons.favico);
  cache.set('closedDirectory', <FcFolder />);
  cache.set('openDirectory', <FcOpenedFolder />);

  return function (
    extension: string,
    name: string,
    fullName: string
  ): ReactNode {
    if (cache.has(fullName)) return cache.get(fullName);
    else if (cache.has(extension)) return cache.get(extension);
    else if (cache.has(name)) return cache.get(name);
    else return <FcFile />;
  };
}

export const getIcon = getIconHelper();
